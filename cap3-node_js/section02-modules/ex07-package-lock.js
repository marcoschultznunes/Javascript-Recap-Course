/*
    npm init:
        command that initializes a 'wizard' to setup the properties of the package-lock file

    npm init -y:
        responds yes to all those questions in the 'wizard' and skips them
     
    npm i:
        installs all node modules

    npm i --save (package name):
        installs the specified package and creates the node_modules folder

    npm i --save-dev (package name):
        does the same, but adds the dependency stating that it's for development only

    -E:
        flag that tells npm to install the exact specified version
*/